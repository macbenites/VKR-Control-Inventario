package Modelo;

public class DetalleIngreso {

    private int p_codigo;
    private String p_descripcion;
    private String p_marca;
    private String p_modelo;
    private String p_um;
    private int p_stock;
    private int p_cantidad;

    public int getP_codigo() {
        return p_codigo;
    }

    public void setP_codigo(int p_codigo) {
        this.p_codigo = p_codigo;
    }

    public String getP_descripcion() {
        return p_descripcion;
    }

    public void setP_descripcion(String p_descripcion) {
        this.p_descripcion = p_descripcion;
    }

    public String getP_marca() {
        return p_marca;
    }

    public void setP_marca(String p_marca) {
        this.p_marca = p_marca;
    }

    public String getP_modelo() {
        return p_modelo;
    }

    public void setP_modelo(String p_modelo) {
        this.p_modelo = p_modelo;
    }

    public String getP_um() {
        return p_um;
    }

    public void setP_um(String p_um) {
        this.p_um = p_um;
    }

    public int getP_stock() {
        return p_stock;
    }

    public void setP_stock(int p_stock) {
        this.p_stock = p_stock;
    }

    public int getP_cantidad() {
        return p_cantidad;
    }

    public void setP_cantidad(int p_cantidad) {
        this.p_cantidad = p_cantidad;
    }
    
    

}
